class Member {
  final String id;
  final String fullName;
  final String phone;
  final int age;
  final double weight;
  final double height;
  final DateTime createdAt;

  Member({
    required this.id,
    required this.fullName,
    required this.phone,
    required this.age,
    required this.weight,
    required this.height,
    required this.createdAt,
  });
}
